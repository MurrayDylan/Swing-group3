function CountryDropdown({ onSave }) {
    // State used to store the selected country
    const [selectedCountry, setSelectedCountry] = useState('');

    // Handler to update selected country
    const handleChange = (event) => {
        setSelectedCountry(event.target.value);
    };

    // Handler to save selected country
    const handleSave = () => {
        onSave(selectedCountry);
    };

    return (
        <div>
            <select
                value={selectedCountry}
                onChange={handleChange}
                className="form-select"
                style={{ width: '300px', height: '45px', marginBottom: '5px' }}

            >
                <option value="">Select a country</option>
                {countries.map((country, index) => (
                    <option key={index} value={country}>{country}</option>
                ))}
            </select>
            {/* Button to save selected country */}
            <button onClick={handleSave}>Save</button>

        </div>
    );
}

export default CountryDropdown;
